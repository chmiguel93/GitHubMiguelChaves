    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        intCambioAceite=intKilometrajeActual-intKilometrajeCambioDeAceite;
        
        if(Monogrado.isSelected())
        {
          if((intCambioAceite==5000)||(intCambioAceite>5000))//la condicion es usando los 5000 kilometros del limite
            {
              Resultado.setText("MARCA: "+StringMarca+"\n\n"+"MODELO:  "+intModelo+"\n\n"+"El vehiculo ha redorrido "+ intCambioAceite+" kilometros desde\n el ultimo cambio.\n\nEs recomendable realizar un cambio de aceite\nen este momento, ya que se ha recorrido mas\nde 5000 kilometros");                                  
             }else
                  {
                    Resultado.setText("MARCA:  "+StringMarca+"\n\n"+"MODELO:  "+intModelo+"\n\n"+"El vehiculo ha recorrido "+intCambioAceite+" kilometros desde\nel ultimo cambio.\n\n"+"No es necesario realizar el cambio en este momento,\nrecuerde que es recomendable cambiar aceite\ndespues de recorrer 5000 kilometros");
                  }
        }// cierre del if monogrado
        else{
             if(Multigrado.isSelected())
                {
                 if((intCambioAceite==10000)||(intCambioAceite>10000))//la condicion es usando los 10000 kilometros del limite
                     {
                        Resultado.setText("MARCA: "+StringMarca+"\n\n"+"MODELO:  "+intModelo+"\n\n"+"El vehiculo ha redorrido "+ intCambioAceite+" kilometros desde\n el ultimo cambio.\n\nEs recomendable realizar un cambio de aceite\nen este momento, ya que se ha recorrido mas\nde 10000 kilometros");                                  
                     }else
                        {
                          Resultado.setText("MARCA:  "+StringMarca+"\n\n"+"MODELO:  "+intModelo+"\n\n"+"El vehiculo ha recorrido "+intCambioAceite+" kilometros desde\nel ultimo cambio.\n\n"+"No es necesario realizar el cambio en este momento,\nrecuerde que es recomendable cambiar aceite\ndespues de recorrer 10000 kilometros");
                        }
                }//cierre if multigrado
                else{
                     JOptionPane.showMessageDialog(this, TipoVehiculo.getmensaje());//imprime el srtring del mensaje creado anteriormente
                    }
            }
    }//GEN-LAST:event_aceptarActionPerformed

